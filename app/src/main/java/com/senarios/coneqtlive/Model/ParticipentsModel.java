package com.senarios.coneqtlive.Model;

public class ParticipentsModel {


    private String name ;
    private String image ;
    private Integer id;

    public ParticipentsModel(String name, String image,Integer id) {
        this.name = name;
        this.image = image;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

}
